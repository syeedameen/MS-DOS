
; SUBROUITNE TO PERFORM SELECTION SORT 
SELSORT:
	POP AX 
	POP SI 
	POP CX 
	PUSH AX 

	XOR AX,AX 						;CLEAR REGISTERS	 
	XOR BX,BX 

REPEAT2_SELSORT:	
	CMP BX,CX 
	JNE SKIP1_SELSORT
	MOV DX,BX 
	MOV AH,[SI+BX]

REPEAT1_SELSORT:
	CMP DX,CX 
	JNE SKIP2_SELSORT

	CMP AH,[SI+BX+DX]
	JC NOSWAP_SELSORT
	XCHG AH,[SI+BX+DX]
	MOV [SI+BX+DX],AH 
NOSWAP_SELSORT:
	INC DX 
	JMP REPEAT1_SELSORT
	MOV DX,BX 
	INC BX 
	JMP REPEAT2_SELSORT
SKIP1_SELSORT:
	RET 
